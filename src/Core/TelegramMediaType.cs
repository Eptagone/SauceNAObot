namespace SauceNAO.Core;

/// <summary>
/// Represents a media type of a Telegram message.
/// </summary>
public enum TelegramMediaType
{
    /// <summary>
    /// Represents an animation.
    /// </summary>
    Animation,

    /// <summary>
    /// Represents a document.
    /// </summary>
    Document,

    /// <summary>
    /// Represents a photo.
    /// </summary>
    Photo,

    /// <summary>
    /// Represents a video.
    /// </summary>
    Video,

    /// <summary>
    /// Represents a sticker.
    /// </summary>
    Sticker,
}
